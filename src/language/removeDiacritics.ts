export function removeDiacritics(value: string): string {
  return value
    .replace(/Æ/gu, 'AE')
    .replace(/æ/gu, 'ae')
    .replace(/À|Á|Â|Ä|Æ|Ã|Å|Ā|Ă|Ą/gu, 'A')
    .replace(/á|å|ä|â|ã|à|ā|ă|ą/gu, 'a')
    .replace(/Ç|Ć|Č|Ĉ|Ċ/gu, 'C')
    .replace(/ç|ć|č|ĉ|ċ/gu, 'c')
    .replace(/đ/gu, 'd')
    .replace(/É|È|Ê|Ë|Ē|Ė|Ę/gu, 'E')
    .replace(/é|è|ê|ë|ē|ė|ę|ě/gu, 'e')
    .replace(/Ĝ/gu, 'G')
    .replace(/ğ|ĝ/gu, 'g')
    .replace(/Ĥ|Ħ/gu, 'H')
    .replace(/ĥ|ħ/gu, 'h')
    .replace(/Í|Ì|Ï|Î|Ī|Į|İ/gu, 'I')
    .replace(/í|ì|ï|î|ī|į|ı/gu, 'i')
    .replace(/Ĵ/gu, 'J')
    .replace(/ĵ/gu, 'j')
    .replace(/Ķ/gu, 'K')
    .replace(/ķ/gu, 'k')
    .replace(/Ĺ|Ł/gu, 'L')
    .replace(/ĺ|ł/gu, 'l')
    .replace(/Ñ|Ń|Ň|Ņ|Ŋ/gu, 'N')
    .replace(/ñ|ń|ň|ņ|ŋ/gu, 'n')
    .replace(/Ó|Ò|Ö|Ô|Õ|Ø|Ō|Ő/gu, 'O')
    .replace(/ó|ò|ö|ô|õ|ø|ō|ő/gu, 'o')
    .replace(/Ŕ|Ř/gu, 'R')
    .replace(/ŕ|ř/gu, 'r')
    .replace(/Š|Ś|Ş|Ŝ/gu, 'S')
    .replace(/š|ś|ş|ŝ/gu, 's')
    .replace(/ß/gu, 'ss')
    .replace(/Ţ/gu, 'T')
    .replace(/ţ/gu, 't')
    .replace(/Ú|Ù|Ü|Û|Ū|Ů|Ű/gu, 'U')
    .replace(/ú|ù|ü|û|ū|ů|ű/gu, 'u')
    .replace(/Ŵ/gu, 'W')
    .replace(/ŵ/gu, 'w')
    .replace(/Ý|Ÿ/gu, 'Y')
    .replace(/ÿ|ý/gu, 'y')
    .replace(/Ž|Ź|Ż/gu, 'Z')
    .replace(/ž|ź|ż/gu, 'z');
}
